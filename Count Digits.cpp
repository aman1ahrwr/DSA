class Solution{
public:
    int evenlyDivides(int N){
        int t = N;
        int cnt=0;
        while(t!=0){
            int d = t%10;
            if(d>0 && N%d==0){
                cnt++;
            }
            t/=10;
        }
        
        return cnt;
    }
};

